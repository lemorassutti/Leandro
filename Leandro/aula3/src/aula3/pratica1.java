package aula3;

public class pratica1 {

	public static void main(String[] args) {
		int valor=0, tres=0;
		
		
		while (valor != 9) {
			valor++;
			tres++;
			if (valor % 3 == 0) {
				System.out.println(valor*-1);
			}else {
				System.out.println(valor);
			}
			
			;
		}
			
			
	}

}
