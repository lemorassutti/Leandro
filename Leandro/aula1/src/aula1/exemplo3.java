package aula1;
//exemplo de tipo de operadores
public class exemplo3 {

	public static void main(String[] args) {
		int n1;
		int n2;
		int res;
		
		n1=10;
		n2=10;
		
		res=n1+n2;
		System.out.println(n1+"+"+n2+"="+res);
		
		res=n1-n2;
		System.out.println(n1+"-"+n2+"="+res);
		
		res=n1*n2;
		System.out.println(n1+"x"+n2+"="+res);
		
		res=n1/n2;
		System.out.println(n1+"/"+n2+"="+res);
		
	}

}
